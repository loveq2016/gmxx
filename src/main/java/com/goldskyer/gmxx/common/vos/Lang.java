package com.goldskyer.gmxx.common.vos;

public class Lang
{
	private String cn;
	private String en;

	public Lang(String cn, String en)
	{
		super();
		this.cn = cn;
		this.en = en;
	}

	public String getCn()
	{
		return cn;
	}

	public void setCn(String cn)
	{
		this.cn = cn;
	}

	public String getEn()
	{
		return en;
	}

	public void setEn(String en)
	{
		this.en = en;
	}

}
